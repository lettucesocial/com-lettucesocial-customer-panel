function filterByZipCode
(
    zipCode
)
    {
        let reuslt = creatorList.filter(
            (
                currentCreator
            )=>
                {
                    if
                    (
                        currentCreator.ZipCode == zipCode
                    )
                        {
                            return currentCreator;
                        }
                }
        );

        return reuslt;
    }

